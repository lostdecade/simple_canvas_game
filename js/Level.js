//====================================================================================
//	LEVEL CLASS
//====================================================================================
Game.Level = (function(win, doc) {

	/**
		@constructor
	*/
	function Level() {
		this.stages = [{ 
			"numEnemies": 3,
			"numBosses": 0,
			"enemyStrength": 1 }, { 
			"numEnemies": 6, 
			"numBosses": 0, 
			"enemyStrength": 1 }];
	}

	//-----------------------------------------------------------------------------------------------------
	//	PRIVATE METHODS
	//-----------------------------------------------------------------------------------------------------
	//-----------------------------------------------------------------------------------------------------

	//-----------------------------------------------------------------------------------------------------
	//	EVENT HANDLERS
	//-----------------------------------------------------------------------------------------------------
	//-----------------------------------------------------------------------------------------------------

	//-----------------------------------------------------------------------------------------------------
	//	PUBLIC METHODS
	//-----------------------------------------------------------------------------------------------------	
	Level.prototype = {
		'get': function(numLevel) {
			//make sure numLevel is within the amount of stages
			if((numLevel < this.stages.length - 1) && (numLevel < 0)) {
				return false;
			}

			return this.stages[numLevel];
		}
	};
	//-----------------------------------------------------------------------------------------------------

	return new Level();

}(window, document));
//====================================================================================